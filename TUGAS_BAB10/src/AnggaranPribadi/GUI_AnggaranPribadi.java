public void koneksi() {
    try {
        conn = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/oop_2418019_anggaranpribadi?serverTimezone=UTC", 
            "root", ""
        );
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Koneksi gagal: " + e.getMessage());
    }
}

public void tampil() {
    DefaultTableModel tabel = new DefaultTableModel();
    tabel.addColumn("Nama");
    tabel.addColumn("Kategori");
    tabel.addColumn("Jumlah");

    try {
        koneksi();
        String sql = "SELECT * FROM tb_anggaranpribadi";
        Statement stat = conn.createStatement();
        ResultSet res = stat.executeQuery(sql);
        while (res.next()) {
            tabel.addRow(new Object[] {
                res.getString("nama"),
                res.getString("kategori"),
                res.getDouble("jumlah")
            });
        }
        tabel_data_anggaran.setModel(tabel);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal Tampil: " + e.getMessage());
    }
}

public void insert() {
    String kategori = cbKategori.getSelectedItem() != null ? cbKategori.getSelectedItem().toString() : "";
    String nama = txtNama.getText().trim();
    String jumlah = txtJumlah.getText().trim();

    if (kategori.equals("") || nama.equals("") || jumlah.equals("")) {
        JOptionPane.showMessageDialog(null, "Harap isi semua data!");
        return;
    }

    try {
        koneksi();
        String sql = "INSERT INTO tb_anggaranpribadi (kategori, nama, jumlah) VALUES (?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, kategori);
        pst.setString(2, nama);
        pst.setDouble(3, Double.parseDouble(jumlah));
        pst.executeUpdate();
        pst.close();
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
        tampil(); // refresh tabel setelah insert
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menyimpan data: " + e.getMessage());
        e.printStackTrace();
    }
}
