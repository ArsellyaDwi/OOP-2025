public void update() {
    try {
        koneksi();
        String sql = "UPDATE tb_anggaranpribadi SET kategori=?, nama=?, jumlah=? "
                   + "WHERE kategori=? AND nama=?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, cbKategori.getSelectedItem().toString());
        pst.setString(2, txtNama.getText());
        pst.setDouble(3, Double.parseDouble(txtJumlah.getText()));
        pst.setString(4, kategoriLama);
        pst.setString(5, namaLama);
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data berhasil diupdate!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal update: " + e.getMessage());
    }
    tampil();
}

