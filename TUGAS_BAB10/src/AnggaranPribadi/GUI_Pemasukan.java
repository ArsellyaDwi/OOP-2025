    public void tampil() {
    DefaultTableModel tabelhead = new DefaultTableModel();
    tabelhead.addColumn("Nama");
    tabelhead.addColumn("Jumlah");

    try {
        koneksi();
        String sql = "SELECT * FROM tb_pemasukan";
        Statement stat = conn.createStatement();
        ResultSet res = stat.executeQuery(sql);
        while (res.next()) {
            tabelhead.addRow(new Object[]{
                res.getString("nama"),
                res.getDouble("jumlah")
            });
        }
        txtTabelAnggaran.setModel(tabelhead);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal koneksi ke database");
    }
}
    
    public void insert() {
    String nama = txtNama.getText();
    String jumlahStr = txtJumlah.getText();

    if (nama.isEmpty() || jumlahStr.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!");
        return;
    }

    try {
        double jumlah = Double.parseDouble(jumlahStr);
        koneksi();
        Statement statement = conn.createStatement();
        String sql = "INSERT INTO tb_pemasukan (nama, jumlah) VALUES ('"+nama+"', "+jumlah+")";
        statement.executeUpdate(sql);
        statement.close();
        JOptionPane.showMessageDialog(null, "Berhasil menambahkan data!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menambahkan data!");
    }
    tampil();  // refresh tabel
    clearForm();
}
   

public void update() {
    String nama = txtNama.getText();
    String jumlahStr = txtJumlah.getText();

    if (nama.isEmpty() || jumlahStr.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!");
        return;
    }

    try {
        double jumlah = Double.parseDouble(jumlahStr);
        koneksi();
        Statement statement = conn.createStatement();
        String namaLama = null;
        String sql = "UPDATE tb_pemasukan SET nama = '"+nama+"', jumlah = "+jumlah+" WHERE nama = '"+namaLama+"'";
        statement.executeUpdate(sql);
        statement.close();
        JOptionPane.showMessageDialog(null, "Berhasil update data!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal update data!");
    }
    tampil(); // refresh tabel
    clearForm();
}
public void delete() {
    int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            koneksi();
            String sql = "DELETE FROM tb_pemasukan WHERE nama = '"+ namaLama+"'";
            java.sql.PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
            stmt.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menghapus data!");
        }
        tampil();
        clearForm();
    }
}

