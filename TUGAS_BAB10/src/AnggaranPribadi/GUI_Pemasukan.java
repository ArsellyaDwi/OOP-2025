    // Ambil input dari form
    try {
    String nama = txtNama.getText();
    double jumlahBaru = Double.parseDouble(txtJumlah.getText());

    // Misalnya saldoAwal kamu set default 0, nanti ditambahkan oleh prosesTransaksi
    double saldoAwal = 0;

    // Polimorfisme: menggunakan referensi abstrak tapi objek nyata turunan
    Anggaran pemasukan = new Pemasukan(saldoAwal);

    // Proses transaksi menggunakan method override
    double saldoBaru = pemasukan.prosesTransaksi(jumlahBaru);

    // Tampilkan hasil ke tabel
    DefaultTableModel model = (DefaultTableModel) txtTabelAnggaran.getModel();
    model.addRow(new Object[]{nama, pemasukan.kategori, jumlahBaru});

    // Tampilkan saldo baru
    jLabel3.setText("Saldo: Rp " + saldoBaru);
    } catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(null, "Jumlah harus berupaÂ angka.");
}
    insert();
